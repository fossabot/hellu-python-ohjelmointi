luku1 = int(input("luku1? "))
luku2 = int(input("luku2?" ))
komento = (input("komento? "))

if komento =="summa":
    print(luku1,"+", luku2, "=" ,luku1 + luku2)
if komento =="tulo":
    print(luku1,"*", luku2, "=" ,luku1 * luku2)
if komento =="erotus":
    print(luku1,"-", luku2, "=" ,luku1 - luku2)        


